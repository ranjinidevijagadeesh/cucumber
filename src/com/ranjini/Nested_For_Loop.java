package com.ranjini;

public class Nested_For_Loop {

	public static void main(String [] args) {
		for (int i = 1; i<=5; i++) 
		{
		for (int j = 1; j<=10; j++)
		{
			System.out.print(j);
			}
		System.out.println(" "+ i);
	}
		{
		for (int a = 1; a>=10; a--)
		{
		for (int b = 5; b>=15; b--)
		{
			System.out.println(b);
			}
		System.out.println(" "+ a);
	}
		
}
}
}
